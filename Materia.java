public class Materia {
    private String nombre;
    private String codigo;
    private String profesorEncargado;
    private float notas;

    public Materia(String nombre, String codigo, String profesorEncargado, float notas) {
        this.nombre = nombre;
        this.codigo = codigo;
        this.profesorEncargado = profesorEncargado;
        this.notas = notas;
    }
}
